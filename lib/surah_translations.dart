// Helper function to get surah name by number and language
String getSurahName(int number, String languageCode) {
  if (surahNames.containsKey(number)) {
    if (languageCode == 'fr') {
      return surahNames[number]!['fr'] ?? surahNames[number]!['en'] ?? 'Sourate $number';
    }
    return surahNames[number]!['en'] ?? 'Surah $number';
  }
  return languageCode == 'fr' ? 'Sourate $number' : 'Surah $number';
}

// Helper function to get surah translation by number and language
String getSurahTranslation(int number, String languageCode) {
  if (surahTranslations.containsKey(number)) {
    if (languageCode == 'fr') {
      return surahTranslations[number]!['fr'] ?? '';
    }
    return surahTranslations[number]!['en'] ?? '';
  }
  return '';
}

